public class MainActivity extends AppCompatActivity {

    private static int SPLASH_TIME = 1750;
    private ConstraintLayout layout;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        getWindow().setFlags(WindowManager.LayoutParams.FLAG_LAYOUT_NO_LIMITS,WindowManager.LayoutParams.FLAG_LAYOUT_NO_LIMITS);
        layout = (ConstraintLayout) findViewById(R.id.back);
        if(getThem().equals("Dark"))
        {
            layout.setBackgroundColor(Color.parseColor("#000000"));
        }

        FirebaseMessaging.getInstance().setAutoInitEnabled(true);
        FirebaseInstanceId.getInstance().getInstanceId().addOnCompleteListener(new OnCompleteListener<InstanceIdResult>() {
            @Override
            public void onComplete(@NonNull Task<InstanceIdResult> task) {
                if(!task.isSuccessful()){
                    Log.e("taskUnSuccessfull","check");
                    return;
                }
                String token = task.getResult().getToken();
                Log.e("TokenFile",token);
            }
        });
        new Handler().postDelayed(new Runnable() {
            @Override
            public void run() {
                Intent intent = new Intent(MainActivity.this,IntroActivity.class);
                startActivity(intent);
                finish();
            }
        },SPLASH_TIME);



    }



    private String getThem() {
        SharedPreferences prefs = getApplicationContext().getSharedPreferences("PLAY_API",MODE_PRIVATE);
        return prefs.getString("Theme","Light");
    }

}
